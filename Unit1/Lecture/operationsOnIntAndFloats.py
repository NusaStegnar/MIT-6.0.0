""" OPERATORS ON ints and floats

 i + j  the sum
 i - j  the difference
 i * j  the product
 i / j  division
 i // j  int division
 i % j  the remainder when i is divided by j
 i ** j  i to the power of j


SIMPLE OPERATIONS

 parentheses used to tell Python to do these 
operations first
◦ 3 * 5 + 1 evaluates to 16
◦ 3 * (5 + 1) evaluates to 18
 operator precedence without parentheses
◦ **
◦ *
◦ /
◦ + and – executed left to right, as appear in expression """

print(3 + 2)
print(3.0 + 4)
print(3.4 + 8.2)
print(4 * 5)
print(4.5 * 4)
print(5 / 3)
print(16 / 4)
print(5 // 3)
print(5.2 // 3)
print(5.2 // 4.1)
print(5 % 3)
print(5.3 % 4)
print(5.3 % 4.6)
print(3 * 5 + 1)
print(3 * (5 + 1))
