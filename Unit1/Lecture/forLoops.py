""" CONTROL FLOW: for LOOPS

for <variable> in range(<some_num>):
    <expression>
    <expression> 
...
 each time through the loop, <variable> takes a value
 first time, <variable> starts at the smallest value
 next time, <variable> gets the prev value + 1
 etc. """

